// Helpers
export const getNombrePais = (codigo) => {
  const pais = PAISES.find(p => p.codigo === codigo);
  return pais ? pais.nombre : codigo;
};

export const getNombreMoneda = (codigo) => {
  const moneda = MONEDAS.find(m => m.codigo === codigo);
  return moneda ? moneda.nombre : codigo;
};

export const getNombreUnidadMedida = (codigo) => {
  const um = UNIDADES_MEDIDA.find(u => u.codigo === codigo);
  return um ? `${um.nombre} (${um.simbolo})` : codigo;
};

export const getNombreCPBS = (codigo) => {
  const cpbs = CPBS_SEGMENTOS.find(c => c.codigo === codigo);
  return cpbs ? cpbs.nombre : codigo;
};

